        private static IEnumerable<int> GeneratePowerSetInt32(Type enumType)
        {
            var values = Enum.GetValues(enumType).Cast<int>().Where(v => v != 0).Distinct().ToArray();
            switch (values.Length)
            {
                case 0: return GeneratePowerSet();
                case 1: return GeneratePowerSet(values[0]);
                case 2: return GeneratePowerSet(values[0], values[1]);
                case 3: return GeneratePowerSet(values[0], values[1], values[2]);
                case 4: return GeneratePowerSet(values[0], values[1], values[2], values[3]);
                case 5: return GeneratePowerSet(values[0], values[1], values[2], values[3], values[4]);
                case 6: return GeneratePowerSet(values[0], values[1], values[2], values[3], values[4], values[5]);
                case 7: return GeneratePowerSet(values[0], values[1], values[2], values[3], values[4], values[5], values[6]);
