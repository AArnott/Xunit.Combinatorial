    /// <summary>
    /// When placed on a parameter with a <see cref="FlagsAttribute"/> enum type, generates all the possible flag combinations for the enum
    /// </summary>
    public sealed class AllFlagsAttribute : ParameterValuesAttribute
    {
        private static MethodInfo GeneratePowerSetMethod { get; } = typeof(AllFlagsAttribute).GetTypeInfo()
            .DeclaredMethods.Single(m => m.IsGenericMethod && m.Name == nameof(GeneratePowerSet) && m.GetGenericArguments().Length == 1 && m.GetParameters().Length == 0);

        /// <inheritdoc />
        public override IEnumerable<object> GetValues(ParameterInfo parameter)
        {
            var paramType = parameter.ParameterType;
            var paramTypeInfo = paramType.GetTypeInfo();

            if (!paramTypeInfo.IsEnum)
            {
                throw new InvalidOperationException(string.Format(Strings.AllFlagsOnlyOnEnums, nameof(AllFlagsAttribute)));
            }

            if (paramTypeInfo.GetCustomAttribute<FlagsAttribute>() == null)
            {
                throw new InvalidOperationException(string.Format(Strings.AllFlagsOnlyOnFlagsEnums, nameof(AllFlagsAttribute)));
            }

            return (IEnumerable<object>)GeneratePowerSetMethod.MakeGenericMethod(paramType).Invoke(null, null);
        }

        private static IEnumerable<object> GeneratePowerSet<TEnum>()
        {
            var underlyingType = Enum.GetUnderlyingType(typeof(TEnum));
            if (underlyingType == typeof(byte))
            {
                return GeneratePowerSetByte(typeof(TEnum)).Cast<TEnum>().Cast<object>();
            }
            else if (underlyingType == typeof(sbyte))
            {
                return GeneratePowerSetSByte(typeof(TEnum)).Cast<TEnum>().Cast<object>();
            }
            else if (underlyingType == typeof(ushort))
            {
                return GeneratePowerSetUInt16(typeof(TEnum)).Cast<TEnum>().Cast<object>();
            }
            else if (underlyingType == typeof(short))
            {
                return GeneratePowerSetInt16(typeof(TEnum)).Cast<TEnum>().Cast<object>();
            }
            else if (underlyingType == typeof(uint))
            {
                return GeneratePowerSetUInt32(typeof(TEnum)).Cast<TEnum>().Cast<object>();
            }
            else if (underlyingType == typeof(int))
            {
                return GeneratePowerSetInt32(typeof(TEnum)).Cast<TEnum>().Cast<object>();
            }
            else if (underlyingType == typeof(ulong))
            {
                return GeneratePowerSetUInt64(typeof(TEnum)).Cast<TEnum>().Cast<object>();
            }
            else
            {
                return GeneratePowerSetInt64(typeof(TEnum)).Cast<TEnum>().Cast<object>();
            }
        }

        #region On-the-fly powersets

        private static IEnumerable<byte> GeneratePowerSetByte(Type enumType)
        {
            var values = Enum.GetValues(enumType).Cast<byte>().Where(v => v != 0).Distinct().ToArray();
            return GeneratePowerSet(values, 0);
        }

        private static IEnumerable<byte> GeneratePowerSet(byte[] values, int depth)
        {
            if (depth == values.Length)
            {
                yield return 0;
                yield break;
            }

            foreach (var flagCombo in GeneratePowerSet(values, depth + 1))
            {
                yield return flagCombo;
                yield return (byte)(flagCombo | values[depth]);
            }
        }

